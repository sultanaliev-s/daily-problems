// https://codeforces.com/problemset/problem/893/B

package main

import "fmt"

func main() {
	beauty := []int{
		0b1,
		0b110,
		0b11100,
		0b1111000,
		0b111110000,
		0b11111100000,
		0b1111111000000,
		0b111111110000000,
		0b11111111100000000,
		0b1111111111000000000,
		0b111111111110000000000,
		0b11111111111100000000000,
		0b1111111111111000000000000,
		0b111111111111110000000000000,
		0b11111111111111100000000000000,
		0b1111111111111111000000000000000,
	}

	var n int
	fmt.Scan(&n)

	div := 1
	for i := range beauty {
		if n%beauty[i] == 0 {
			div = beauty[i]
		}
	}

	fmt.Println(div)
}
